package com.rays.basic;

public class FindLargestNumberInArray {
 public static void main(String[] args) {
	
	 int [] arr = {5,10,155,20,25,30};
	   
	  int max = arr[0];
	 for (int i = 1; i < arr.length; i++) {
         if (arr[i] > max) {
             max = arr[i];
         
     }
    
  
     System.out.println("Highest number is: " + max);
 }

	
    	   
    	    	
    	    	
    	    }
    	   
}
  
	
	 
	  
	
	 
	 



